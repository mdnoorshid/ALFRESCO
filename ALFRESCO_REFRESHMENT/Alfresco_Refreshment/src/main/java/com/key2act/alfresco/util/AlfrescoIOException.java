package com.key2act.alfresco.util;
public class AlfrescoIOException extends Exception {

	public AlfrescoIOException() {
		super();
	}

	public AlfrescoIOException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public AlfrescoIOException(String message, Throwable cause) {
		super(message, cause);
	}

	public AlfrescoIOException(String message) {
		super(message);
	}

	public AlfrescoIOException(Throwable cause) {
		super(cause);
	}

}
